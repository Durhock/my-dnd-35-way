let _skavenToastInterval = null;

function _startSkavenToasts(lang) {
  // Clear any existing interval
  if (_skavenToastInterval) clearInterval(_skavenToastInterval);
  // Only run while in Skaven mode
  function _skavenToast() {
    if (_lang !== 'sken' && _lang !== 'skfr') {
      clearInterval(_skavenToastInterval);
      _skavenToastInterval = null;
      return;
    }
    const msgs = (lang === 'fr') ? _SK_TOASTS_FR : _SK_TOASTS_EN;
    const msg = msgs[Math.floor(Math.random() * msgs.length)];
    if (typeof _ee !== 'undefined' && _ee.toast) _ee.toast(msg);
  }
  // First toast after 8s, then every 35-70s randomly
  setTimeout(() => {
    _skavenToast();
    _skavenToastInterval = setInterval(() => {
      if (Math.random() < 0.6) _skavenToast();
    }, 45000);
  }, 8000);
}

// Apply translations to all data-i18n elements
function _applyLang() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });
  // Update the lang button label
  const lbl = document.getElementById('lang-btn-label');
  if (lbl) lbl.textContent = t('lang_label');
  // Update document lang attribute
  document.documentElement.lang = _lang === 'zh' ? 'zh' : _lang === 'ko' ? 'ko' : _lang;
  // Highlight active language option in dropdown and About page
  document.querySelectorAll('[data-lang]').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === _lang);
  });
  // Re-apply translations to any dynamically rendered data-i18n elements
  // (runs after DOM updates settle)
  setTimeout(() => {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      const val = t(key);
      if (val && val !== key) el.textContent = val;
    });
  }, 50);

  // Fun language visual effects on header logo
  const logo = document.querySelector('.app-title');
  if (logo) {
    if (_lang === 'kl') {
      logo.style.filter = 'hue-rotate(270deg)';
      logo.style.textShadow = '';
    } else if (_lang === 'sken' || _lang === 'skfr') {
      logo.style.filter = 'hue-rotate(80deg) saturate(1.5)';
      logo.style.textShadow = '0 0 12px rgba(100,200,50,0.5)';
    } else {
      logo.style.filter = '';
      logo.style.textShadow = '';
    }
  }
  // Body Skaven class for potential CSS chaos
  document.body.classList.toggle('mode-skaven', _lang === 'sken' || _lang === 'skfr');
}

// Open language picker dropdown
function openLangPicker() {
  const picker = document.getElementById('lang-picker-dropdown');
  if (!picker) return;
  picker.style.display = picker.style.display === 'none' ? 'block' : 'none';
}

