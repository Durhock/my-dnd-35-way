function t(key) {
  return (I18N[_lang] && I18N[_lang][key]) || (I18N['fr'][key]) || key;
}

function setLang(code) {
  // Fun language easter eggs
  if (code === 'kl') {
    _showKlingonPopup();
    _lang = 'kl';
  } else if (code === 'sken') {
    _showSkavenPopup('en');
    _lang = 'sken';
  } else if (code === 'skfr') {
    _showSkavenPopup('fr');
    _lang = 'skfr';
  } else {
    _lang = code;
  }
  try { localStorage.setItem('dnd35_lang', _lang); } catch(e) {}
  _applyLang();
  // Re-render current visible tab to pick up any t() calls
  const active = document.querySelector('.tab-content:not(.hidden)');
  if (active) {
    const id = active.id.replace('tab-','');
    if (typeof showTab === 'function') showTab(id);
  }
  // Update lang dropdown
  const sel = document.getElementById('lang-select');
  if (sel) sel.value = _lang;
}

